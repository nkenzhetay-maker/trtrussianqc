exports.handler = async function(event) {
  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, body: 'Method Not Allowed' };
  }

  let body;
  try {
    body = JSON.parse(event.body);
  } catch {
    return { statusCode: 400, body: JSON.stringify({ error: 'Invalid JSON' }) };
  }

  const { text, contentType } = body;

  if (!text || text.trim().length < 10) {
    return { statusCode: 400, body: JSON.stringify({ error: 'Текст слишком короткий' }) };
  }

  // Truncate to prevent timeout — ~6000 chars is safe for Haiku within 10s
  const safeText = text.length > 6000 ? text.substring(0, 6000) + '\n\n[Текст обрезан для обработки]' : text;

  const typeLabels = {
    news: 'новость', video: 'видеоматериал',
    infographic: 'инфографика', article: 'статья', social: 'пост для соцсетей'
  };

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-haiku-4-5-20251001',
        max_tokens: 8192,
        system: SYSTEM_PROMPT,
        messages: [{
          role: 'user',
          content: `Проверь следующий материал TRT Russian (тип: ${typeLabels[contentType] || 'материал'}):\n\n${safeText}`
        }]
      })
    });

    if (!response.ok) {
      const err = await response.text();
      console.error('Claude API error:', err);
      return { statusCode: 502, body: JSON.stringify({ error: 'API error' }) };
    }

    const data = await response.json();
    const rawText = data.content?.[0]?.text || '';

    let result;
    try {
      const clean = rawText.replace(/```json|```/g, '').trim();
      result = JSON.parse(clean);
    } catch {
      console.error('JSON parse error:', rawText);
      return {
        statusCode: 200,
        body: JSON.stringify({
          verdict: 'warn',
          correctedText: '',
          issues: { terminology: [], turkeyPosition: [], grammar: [], style: [] },
          generalComment: 'Система не смогла разобрать ответ. Попробуйте снова.'
        })
      };
    }

    return {
      statusCode: 200,
      headers: {
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*'
      },
      body: JSON.stringify(result)
    };

  } catch (err) {
    console.error('Function error:', err);
    return { statusCode: 500, body: JSON.stringify({ error: err.message }) };
  }
};
